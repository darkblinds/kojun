    @staticmethod
    def getRandomBoardAndGroup():
        selectedBoardAndGroup = RandomBoardSelector.boards[random.randint(0, 4)]
        selectedBoard = selectedBoardAndGroup["boardValues"]
        selectedGroup = selectedBoardAndGroup["boardGroups"]
        cellsToKeep = []
        while len(cellsToKeep) < 4:
            position = (
                random.randint(0, len(selectedBoard) - 1),
                random.randint(0, len(selectedBoard) - 1),
            )
            if position in cellsToKeep:
                continue
            cellsToKeep.append(position)

        for i in range(len(selectedBoard)):
            for j in range(len(selectedBoard)):
                if (i, j) in cellsToKeep:
                    continue
                selectedBoard[i][j] = 0
        return selectedBoard, selectedGroup
